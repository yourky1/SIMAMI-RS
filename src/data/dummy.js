// Summary stats
export const getDashboardStats = () => {
  const total = asets.length;
  const tersedia = asets.filter(a => a.status === STATUS_ASET.TERSEDIA).length;
  const dipinjam = asets.filter(a => a.status === STATUS_ASET.DIPINJAM).length;
  const maintenance = asets.filter(a => a.status === STATUS_ASET.MAINTENANCE).length;
  const bermasalah = asets.filter(a => a.status === STATUS_ASET.RUSAK || a.status === STATUS_ASET.KALIBRASI).length;
  return { total, tersedia, dipinjam, maintenance, bermasalah };
};

export const getAsetByKategori = () => {
  const map = {};
  asets.forEach(a => {
    map[a.kategori] = (map[a.kategori] || 0) + 1;
  });
  return Object.entries(map).map(([name, value]) => ({ name, value }));
};

export const getStatusChartData = () => {
  const stats = getDashboardStats();
  return [
    { name: 'Tersedia', value: stats.tersedia, color: '#32a86d' },
    { name: 'Dipinjam', value: stats.dipinjam, color: '#2563eb' },
    { name: 'Maintenance', value: stats.maintenance, color: '#f59e0b' },
    { name: 'Bermasalah', value: stats.bermasalah, color: '#ef4444' },
  ];
};
